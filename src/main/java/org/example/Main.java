package org.example;

public class Main {
    public static void main(String[] args) {
         int friends=10;
         friends=friends-1;
        System.out.println(friends);
         friends=friends+1;
        System.out.println(friends);
         friends=friends*9;
        System.out.println(friends);
         friends=friends/2;
        System.out.println(friends);
         friends=friends%2;
         System.out.println(friends);
         //increment operator
        friends++;
        System.out.println(friends);
        friends--;
        System.out.println(friends);
        //integer division
        double me=100;
        me=me/3;
        System.out.println(me);
          }
}